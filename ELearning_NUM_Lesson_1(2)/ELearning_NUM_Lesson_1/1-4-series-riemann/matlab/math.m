% Not commutative

%  1 - 1/2  + 1/3 - 1/4 + 1/5 - 1/6 + 1/7 - 1/8 + 1/9 - 1/10 + 1/11 - 1/12 ... = ln 2 = 0.69

% (1 - 1/2) - 1/4 + (1/3 - 1/6) - 1/8 + (1/5 - 1/10) - 1/12 ... = ln 2 = 0.69

% (    1/2) - 1/4 + (      1/6) - 1/8 + (      1/10) - 1/12 ... = 1/2 ln 2 = 0.35

func(100)

function x = func(n)
    x = 0;

    for i = 1:n
        %{
        if mod(i, 2) == 0
            x = x - 1/i;
        else
            x = x + 1/i;
        end
        %}
        
        if mod(i, 2) == 0
            x = x - 1/(2*i);
            % fprintf("-% 1.2f\n", 1/(2*i));
        else
            x = x + 1/(2*i);
            % fprintf("+% 1.2f\n", 1/(2*i));
        end
        
    end
end